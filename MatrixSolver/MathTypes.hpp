#ifndef _MATHTYPE_HPP_01092006_
#define _MATHTYPE_HPP_01092006_

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \file
/// \brief Mathematics type definitions
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "CountedArray.hpp"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief Alias for an unsigned integer.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
typedef unsigned int uint;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// \brief Alias for an unsigned long.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
typedef unsigned long ulong;


namespace Math
{
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Function pointer to a function with one double as a parameter.
  /// \ingroup func
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef double (*FunctionPtr1D)(const double);
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Function pointer to a function with two doubles as parameters.
  /// \ingroup func
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef double (*FunctionPtr2D)(const double, const double);
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Function pointer to a function with three doubles as parameters.
  /// \ingroup func
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef double (*FunctionPtr3D)(const double, const double, const double);
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Function pointer to a function with four doubles as parameters.
  /// \ingroup func
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef double (*FunctionPtr4D)(const double, const double, const double, const double);
  
  typedef double (*FunctionPtr5D)(const double, const double, const double, const double, const double);
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Reference counted array of doubles.
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef CountedArray<double> DoubleArray;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Reference counted array of ints.
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef CountedArray<int> IntegerArray;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Reference counted array of bools.
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef CountedArray<bool> BooleanArray;
  
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /// \brief Reference counted array of 1-D Function Pointers.
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  typedef CountedArray<FunctionPtr1D> Function1DArray; 

}

#endif

